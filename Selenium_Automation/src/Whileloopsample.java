
public class Whileloopsample {

	public static void main(String[] args) {
		//for (int i = 0; i < args.length; i++) {
			
		int i=0;
		while (i<2) {
			System.out.println(i);
			i++;
		}
		
		System.out.println("out of while loop");
		
		
		//do while loop
		do {
			System.out.println(i);
			i++;
		} while (i<3);

	}

}
